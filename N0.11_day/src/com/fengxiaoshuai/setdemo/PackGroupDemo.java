package com.fengxiaoshuai.setdemo;

import java.util.ArrayList;

public class PackGroupDemo {
    // 包装类
    // byte    --> Byte
    // short   --> Short
    // char    --> Character
    // int     --> Integer
    // long    --> Long
    // float   --> Float
    // double  --> Double
    // boolean --> Boolean
    public static void main(String[] args) {
        ArrayList<Integer> arr_list = new ArrayList<>();
        arr_list.add(1);
        arr_list.add(99);
        arr_list.add(108);
        arr_list.add(888);
        System.out.println(arr_list);
    }

}
