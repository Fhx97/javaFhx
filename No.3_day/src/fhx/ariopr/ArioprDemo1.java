package fhx.ariopr;


public class ArioprDemo1 {
    public static void main(String[] args) {
        System.out.println(3+2);
        System.out.println(5-1);
        System.out.println(6*9);
        System.out.println(10 / 3);
        System.out.println(10.0 /3 );
        // 整数参与计算,结果只能得到整数
        // 小数参与计算,结果有可能不精确
        System.out.println(10 % 3);
        System.out.println(12.6 % 5);

    }
}
