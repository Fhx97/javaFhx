public class VariableDemo{
	public static void main(String[] args){
		// 定义变量
		// 数据类型 变量名 = 数据组;限定了变量能存储数据的类型
		// int(整数),double(小数)
		// 变量名:存储空间的名字
		// 方便以后使用
		// 等号:赋值;把右边的数据赋给左边的变量
		int a = 10;
		int b = 20;
		a = 30;
		System.out.println(a);
		System.out.println(a+b);
		// 只能存一个值
		// 变量名不允许重复定义
		// 一条语句可以定义多个变量
		// 变量在使用之前一定要进行赋值
		// 变量的作用域范围
	}
}